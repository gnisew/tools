	function getLang () {
		if (_lang) return _lang;

		if (w.localStorage) {
			_lang = w.localStorage.getItem ('pk_lang');
		}

		if (!_lang) {
			var nav = (w.navigator.language || '').toLowerCase ();
			if (nav.indexOf ('zh') === 0) {
				_lang = 'zh-TW';
			} else if (nav.indexOf ('kasu') === 0) {
				_lang = 'kasu';
			} else {
				_lang = 'en';
			}
		}

		return _lang;
	}

	function setLang ( lang ) {
		_lang = lang;
		if (w.localStorage) {
			w.localStorage.setItem ('pk_lang', lang);
		}
	}

	function t ( key ) {
		var lang = getLang ();
		var dict = translations[lang] || translations['en'];
		return dict[key] || translations['en'][key] || key;
	}

